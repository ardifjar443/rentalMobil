    private void prosesLogin() {
       try {
            LoginService loginService = new DatabaseLoginService();
            String username = jUsername.getText();
            char[] pass = jPassword.getPassword();
            String password = new String(pass);
            
            boolean loginResult = loginService.login(username, password);
            if (loginResult){
                dataUser data = new dataUser(username);
                Menu menu = new Menu(username);
                menu.setVisible(true);
                
                dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Username dan Password salah","Pesan",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
    }
    private void prosesLoginAdmin() {
       try {
            LoginService loginService = new DatabaseLoginServiceAdmin();
            String username = jUsername1.getText();
            char[] pass = jPassword1.getPassword();
            String password = new String(pass);
            
            boolean loginResult = loginService.login(username, password);
            if (loginResult){
                JOptionPane.showMessageDialog(null, "Berhasil Login","Pesan",JOptionPane.INFORMATION_MESSAGE);
                menuAdmin menu = new menuAdmin(username);
                menu.setVisible(true);
                
                
                
                dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Username dan Password salah","Pesan",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
    }
